package model;

public class Disparo {
	
	private int x;
	private int y;
	private boolean shoot;
    
    
    //--------------------------------------------------------------------------------------------------------------------------------------------------------
    
    
	public Disparo(int x, int y) {
		this.x = x;
		this.y = y;
		this.shoot = false;
	}
    
    
    //--------------------------------------------------------------------------------------------------------------------------------------------------------
    
    
	public int getX() {
		return x;
	}
    
    
    //--------------------------------------------------------------------------------------------------------------------------------------------------------
    
    
	public int getY() {
		return y;
	}
    
    
    //--------------------------------------------------------------------------------------------------------------------------------------------------------
    
    
	public boolean isShooting() {
		return shoot;
	}
    
    
    //--------------------------------------------------------------------------------------------------------------------------------------------------------
    
    
	public void setShoot(boolean b) {
		this.shoot = b;
	}
    
    
    //--------------------------------------------------------------------------------------------------------------------------------------------------------
    
    
	public void subir() { 	
		if (shoot) {	
			y--;				//Cuando se dispare 
								//Arriba y=0, abajo y=99
			
			if (y<0) {			//Cuando desaparezca del frame
				shoot = false;
			}
		}
	}
    
    
    //--------------------------------------------------------------------------------------------------------------------------------------------------------
    
    
}
	
